package package1;
public class relationaloperators {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("value of a= "+a+" b= "+b);
        System.out.println("a==b "+(a==b));//equalsto
        System.out.println("a<=b "+ (a<=b));//less than or equalsto     
        System.out.println("a>=b "+(a>=b)); //greater than or equalsto
        System.out.println("a>b "+(a>b)); //greater than
        System.out.println("a<b "+(a<b));//lesser than 
        System.out.println("a!=b "+(a!=b));//not equalto 

    }
    
}
