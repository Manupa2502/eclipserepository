package package1;
public class arithmaticoperations {
    public static void main(String[] args) {
        
        int a=10;
        int b=20;
        System.out.println(a+b); //addition
        System.out.println(b-a);//subtraction
        System.out.println(a*b);//multiplication
        System.out.println(b/a);//division
        System.out.println(b%a);//remainder
        System.out.println(a+=b);//a=a+b;addition and assignmnet at same statement 
        System.out.println(a-=b);//a=a-b;subtraction and assignment at same statement
        System.out.println(a*=b);//a=a*b;multiplication and assignment at same statement
        System.out.println(a/=b);//a=a/b;division and assignment at same statement
        System.out.println(a%=b);//a=a%b;get remainder and assign to a
        System.out.println(a++);//post increment
        System.out.println(++a);//pre increment
        System.out.println(a--);//post decrement
        System.out.println(--a);//pre decrement 

    }
    
}
