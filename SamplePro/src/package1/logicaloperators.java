package package1;
public class logicaloperators {
    public static void main(String[] args) {
        boolean a=true;
        boolean b=false;
        System.out.println("a="+a+" and b="+b);
        System.out.println("a&&b "+(a&&b));
        System.out.println("a||b "+(a||b));
        System.out.println("!a "+(!a));
    }
}
